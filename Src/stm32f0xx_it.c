void EXTI4_15_IRQHandler(void)
{
	AdapterBoard *adapter = GetAdapterBoard();
	IRQn_Type irq = EXTI4_15_IRQn;

	for (uint8_t i = 0; i < adapter->irqListSz; i++)
	{
		if (adapter->irqList[i] == irq)
		{
			adapter->IRQ_Handler(irq);
			break;
		}
	}
}

